% --- Executes on button press in pushbutton2.
function pushbutton2_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton2 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)

FigNum=str2num(get(handles.edit3,'string'));
%set file names: prefix
ImgNamePr=get(handles.edit2,'string');
if isempty(ImgNamePr)
    ImgNamePr=['Fig_',num2str(FigNum),'_sub'];
end
%set resolution
dpi=['-r',get(handles.edit4,'string')];


h_figure=get(FigNum,'Children');
len=length(h_figure);
h_figure=sort(h_figure);
if get(handles.checkbox1,'value')
    n=str2num(get(handles.n,'string'));
    m=str2num(get(handles.m,'string'));
    if n*m~=len
        errordlg('n*m should be the row number and column number respectively.');
    end
    for j=1:n
        for k=1:m
            i=(j-1)*m+k;
            set(h_figure(i),'Color','none');
            ImgName=[ImgNamePr,'_0',num2str(j),...
                '_0',num2str(k),'.png'];
            export_fig(h_figure(i),ImgName,'-transparent',dpi);
            
        end
    end
else
    for i=1:len
        set(h_figure(i),'Color','none');
        if i<10
            ImgName=[ImgNamePr,'_0',num2str(i),'.png'];
        else
            ImgName=[ImgNamePr,'_',num2str(i),'.png'];
        end
        export_fig(h_figure(i),ImgName,'-transparent',dpi);
    end
    
end



